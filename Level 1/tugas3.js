/* Tipe data di Javascript adalah:
1. String: Digunakan untuk menyimpan serangkaian karakter/huruf.
2. Integer/Number: Tipe data yang berisikan bilangan bulat.
3. Float: Tipe data yang berisikan bilangan pecahan.
4. Boolean: Tipe data yang mempunyai dua nilai yakni, True atau False.
5. Object: Sekumpulan list dari tipe data primitif (terkadang juga tipe data reference) yang menyimpan nilai dengan konsep berpasangan name-value.
*/

// Tipe data String di Javascript:
const namaDepan = "Abdullah";
const namaBelakang = "Farauk";
let alamatBaru = "Jalan Demak";

// Tipe data Integer di Javascript:
let umurBaru = 18;
let noHp = 510179;
const tanggalLahir = 17

// Tipe data float di Javascript:
const nomorSatu = 2/3;
const nomorDua = 1.240;
const nomorTiga = 1/2;

// Tipe data boolean di Javascript:
const single = true;
const double = false;
const triple = true;

// Tipe data object di Javascript:
const fullName = {firstName: "Abdullah", lastName: "Farauk"};
const kurungSikut = [];
const kosong = null;

const angkaPertama = 124;
const angkaKedua = 12;
let hasil;

// Penjumlahan
hasil = angkaPertama + angkaKedua;

// Pengurangan
hasil = angkaPertama - angkaKedua;
console.log(hasil);

// Pembagian
hasil = angkaPertama / angkaKedua;
console.log(hasil);

// Perkalian
hasil = angkaPertama * angkaKedua;
console.log(hasil);
